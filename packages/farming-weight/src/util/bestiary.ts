export function getBestiaryLevel(kills: number, bracket: keyof typeof BESTIARY_KILL_BRACKETS, maxTier: number): number {
    const brackets = BESTIARY_KILL_BRACKETS[bracket];
    for (let i = brackets.length - 1; i >= 0; i--) {
        const bracketKills = brackets[i] as number;
        if (kills >= bracketKills) {
            return Math.min(i + 1, maxTier);
        }
    }
    return 0;
}